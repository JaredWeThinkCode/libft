#include "libft.h"

int		ft_nbrlen(int nbr)
{
	int		a;

	a = 0;
	if (nbr == 0)
	{
		a = 1;
		return (a);
	}
	while (nbr)
	{
		nbr = nbr / 10;
		a++;
	}
	return (a);
}
