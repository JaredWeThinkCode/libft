#include "libft.h"

int		ft_nbrlen(long int nbr)
{
	unsigned int		a;

	a = 0;
	if (nbr < 0)
	{
		nbr = nbr * -1;
		a++;
	}
	while (nbr >= 10)
	{
		nbr = nbr / 10;
		a++;
	}
	a++;
	return (a);
}
