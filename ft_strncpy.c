#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t a;

	a = 0;
	while (a < len)
	{
		dst[a] = src[a];
		a++;
	}
	dst[a] = '\0';
	return (dst);
}
