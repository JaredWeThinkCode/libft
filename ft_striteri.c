#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	size_t	a;

	a = 0;
	if (s != NULL)
	{
		while (s[a] != '\0')
		{
			f(a, &s[a]);
			a++;
		}
	}
}
