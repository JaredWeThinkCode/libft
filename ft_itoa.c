#include "libft.h"

char	*ft_itoa(int n)
{
	char	*a;
	int		b;

	b = ft_nbrlen(n);
	if (n == 0)
		return ("0");
	if (n == -2147483648)
		return ("-2147483648");
	if (n < 0)
	{
		n = -n;
		b++;
		a = ft_memalloc(b + 1);
		a[0] = '-';
	}
	else
		a = ft_memalloc(b + 1);
	a[b] = '\0';
	while (n)
	{
		a[b - 1] = n % 10 + '0';
		n = n / 10;
		b--;
	}
	return (a);
}
