#include "libft.h"

int		ft_atoi(const char *str)
{
	int	a;
	int	b;
	int	c;

	a = 0;
	c = 1;
	b = 0;
	while (str[a] != '\0' && (str[a] == ' ' || str[a] == '\n' ||
			str[a] == '\t' || str[a] == '\v' || str[a] == '\f' ||
			str[a] == '\r'))
	{
		a++;
	}
	if (str[a] == '-')
	{
		c = -1;
	}
	if (str[a] == '-' || str[a] == '+')
		a++;
	while (str[a] != '\0' && str[a] >= '0' && str[a] <= '9')
	{
		b = b * 10 + (str[a] - '0');
		a++;
	}
	return (b * c);
}
