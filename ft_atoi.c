#include "libft.h"

int		ft_atoi(const char *str)
{
	int a;
	int b;
	int c;

	a = 0;
	b = 0;
	c = 1;
	if (str[a] == '-')
	{
		c = -1;
		a++;
	}
	while (str[a] != '\0')
	{
		b = b * 10 + str[a] - '0';
		a++;
	}
	return (c * b);
}
