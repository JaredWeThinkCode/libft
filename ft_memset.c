#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			a;
	unsigned char	*r;

	a = 0;
	r = b;
	while (a < len)
	{
		*r = c;
		r++;
		a++;
	}
	return (b);
}
