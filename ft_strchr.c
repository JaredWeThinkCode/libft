#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*a;

	a = (char *)s;
	while (*a != '\0')
	{
		if (*a == c)
		{
			return (a);
		}
		a++;
	}
	return (NULL);
}
