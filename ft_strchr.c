#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*a;
	size_t	b;

	a = (char *)s;
	b = ft_strlen(s) + 1;
	while (b > 0)
	{
		if (*a == c)
		{
			return (a);
		}
		a++;
		b--;
	}
	return (NULL);
}
