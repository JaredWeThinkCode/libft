#include "../incl/libft.h"

char	*ft_strdup(char *str)
{
	char *a;
	char *b;
	char *c;

	a = malloc(ft_strlen(str) + 1);
	b = str;
	c = a;
	while (*b != 0)
	{
		*c = *b;
		c++;
		b++;
	}
	*c = '\0';
	return (c);
}
